# Запрос количества билетов
num_tickets = int(input("Введите количество билетов: "))

# Переменная для хранения общей стоимости заказа
total_cost = 0

# Запрос возраста и расчет стоимости для каждого билета
for i in range(num_tickets):
    age = int(input(f"Введите возраст посетителя {i+1}: "))
    if age < 18:
        ticket_cost = 0  # бесплатно для лиц младше 18 лет
    elif 18 <= age < 25:
        ticket_cost = 990  # 990 руб. для лиц от 18 до 25 лет
    else:
        ticket_cost = 1390  # 1390 руб. для лиц от 25 лет и старше
    total_cost += ticket_cost

# Применение скидки 10%, если количество билетов больше трех
if num_tickets > 3:
    total_cost *= 0.9

# Вывод общей стоимости к оплате
print("Общая стоимость к оплате:", total_cost, "руб.")